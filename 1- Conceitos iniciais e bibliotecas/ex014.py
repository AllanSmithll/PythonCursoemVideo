Celsius = float(input('Informe a temperatura em °C: '))
Fahrenheit = (Celsius * 1.8) + 32
Kelvin = Celsius + 273.15
print('Em °F, a temperatura fica em {}°F;'.format(Fahrenheit))
print('Quando se observa na escala K, temos {}K.'.format(Kelvin))
print('Código programado em 30/12/2021, para converter escalas termométricas.')
